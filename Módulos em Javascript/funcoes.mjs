function mostarIdade(name, idade){
    return `A idade de ${name} é ${idade}`;
}

function mostarCidade(name, cidade){
    return `A cidade de ${name} é ${cidade}`;
}

function mostarhobby(name, hobby){
    return `O hobby de ${name} é ${hobby}`;
}


export { mostarIdade, mostarCidade, mostarhobby};